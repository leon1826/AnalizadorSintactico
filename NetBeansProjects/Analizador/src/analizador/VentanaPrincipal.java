    private void btnsintacticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsintacticoActionPerformed
        // TODO add your handling code here:
        String St = txtentrada.getText();
        Sintax s = new Sintax(new analizador.LexerCup(new StringReader(St)));
        try {
            s.parse();
            jtxtsintactico.setText("Analizado correctamente");
            jtxtsintactico.setForeground(Color.LIGHT_GRAY);
        } catch (Exception ex) {
            Symbol sym = s.getS();
            jtxtsintactico.setText("Error de sintaxis en la linea: "+ (sym.right+1) + " Columna: "+ (sym.left+ 1)+ " Text \"" + sym.value);
            jtxtsintactico.setForeground(Color.RED);
        }
    }//GEN-LAST:event_btnsintacticoActionPerformed

